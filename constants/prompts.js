export function buildRebuttalPrompt(topic, userSide, aiSide, history, userArgument, category = '') {
  const historyText = history
    .map((h, i) => `라운드 ${Math.floor(i / 2) + 1} ${i % 2 === 0 ? '사용자' : 'AI'}: ${h}`)
    .join('\n');

  const absurdNote = category === '억지주장'
    ? '\n※ 이 주제는 억지주장 카테고리입니다. 말도 안 되는 주장도 진지하게 반박하세요.'
    : '';

  return `주제: "${topic}"
사용자 입장: "${userSide}" 지지
AI 입장: "${aiSide}" 지지 (절대 바꾸지 말 것)
${historyText ? `\n이전 대화:\n${historyText}\n` : ''}
사용자 주장: ${userArgument}

"${aiSide}" 입장에서 강하게 반박하세요. 이전 대화에서 이미 사용한 전략과 논점은 피하고 새로운 전략으로 반박할 것.${absurdNote}`;
}

export function buildScorePrompt(topic, userSide, aiSide, history, totalRounds = 5) {
  const historyText = history
    .map((h, i) => `라운드 ${Math.floor(i / 2) + 1} ${i % 2 === 0 ? '사용자' : 'AI'}: ${h}`)
    .join('\n');

  return `주제: "${topic}" (사용자: "${userSide}" vs AI: "${aiSide}")
총 ${totalRounds}라운드 진행됨.

전체 대화:
${historyText}

사용자의 논쟁 실력을 평가해주세요.`;
}
